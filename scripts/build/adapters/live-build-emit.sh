#!/usr/bin/env bash
# scripts/build/adapters/live-build-emit.sh — emit Debian live-build config
# from sovereign-os profile YAML. Alt-A substrate adapter per SDD-003.
#
# Usage: live-build-emit.sh <profile.yaml> <output-dir>
#
# Produces a live-build config/ tree:
#   config/auto/config              — lb_config invocation
#   config/auto/build               — build script
#   config/auto/clean               — clean script
#   config/package-lists/sovereign.list.chroot  — package list
#   config/includes.chroot/         — file overlay (populated by whitelabel render)
#   config/hooks/normal/            — first-boot hook
#   config/bootloaders/grub-pc/     — GRUB theming (optional)

__SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
# shellcheck source=../lib/common.sh
. "${__SCRIPT_DIR}/../lib/common.sh"

profile_yaml="${1:?usage: live-build-emit.sh <profile.yaml> <out-dir>}"
out_dir="${2:?usage: live-build-emit.sh <profile.yaml> <out-dir>}"

require_file "${profile_yaml}"

mkdir -p "${out_dir}"/config/{auto,package-lists,includes.chroot,hooks/normal,bootloaders}

profile_id="$(python3 -c "
import yaml, sys
with open('${profile_yaml}') as f:
    print(yaml.safe_load(f)['identity']['id'])
")"

log_info "emitting live-build config for profile=${profile_id} → ${out_dir}"

# ---- config/auto/config — lb_config arguments ----
cat > "${out_dir}/config/auto/config" <<'EOF'
#!/usr/bin/env bash
# auto-generated by sovereign-os live-build adapter
set -euo pipefail

lb config noauto \
    --distribution trixie \
    --architectures amd64 \
    --binary-images iso-hybrid \
    --bootappend-live "boot=live components quiet splash" \
    --apt-source-archives false \
    --memtest none \
    --iso-volume "Sovereign-OS-${SOVEREIGN_OS_PROFILE:-sain-01}" \
    --iso-application "Sovereign OS" \
    --iso-publisher "cyberpunk042" \
    "$@"
EOF
chmod +x "${out_dir}/config/auto/config"

# ---- config/auto/build ----
cat > "${out_dir}/config/auto/build" <<'EOF'
#!/usr/bin/env bash
set -euo pipefail
lb build "$@"
EOF
chmod +x "${out_dir}/config/auto/build"

# ---- config/auto/clean ----
cat > "${out_dir}/config/auto/clean" <<'EOF'
#!/usr/bin/env bash
set -euo pipefail
lb clean "$@"
EOF
chmod +x "${out_dir}/config/auto/clean"

# ---- package-lists/sovereign.list.chroot — from profile.packages ----
python3 - <<PY > "${out_dir}/config/package-lists/sovereign.list.chroot"
import yaml
with open("${profile_yaml}") as f:
    p = yaml.safe_load(f)
base = (p.get("packages") or {}).get("base") or []
profile_pkgs = (p.get("packages") or {}).get("profile") or []
deny = (p.get("packages") or {}).get("deny") or []

print(f"# auto-generated from profiles/${profile_id}.yaml")
print(f"# base ({len(base)}):")
for pkg in base:
    if any(pkg.startswith(d) for d in deny):
        continue
    print(pkg)
print(f"# profile ({len(profile_pkgs)}):")
for pkg in profile_pkgs:
    if pkg.startswith("linux-image-") or pkg.startswith("linux-headers-"):
        # custom kernel ships separately via includes.chroot
        continue
    if any(pkg.startswith(d) for d in deny):
        continue
    print(pkg)
PY

# ---- hooks/normal/0010-sovereign-first-boot.hook.chroot ----
cat > "${out_dir}/config/hooks/normal/0010-sovereign-first-boot.hook.chroot" <<'EOF'
#!/usr/bin/env bash
# Runs inside the chroot during live-build assembly.
# Installs the first-boot greeting service that runs on installed-system first login.
set -euo pipefail

# Install sovereign-os scripts to /opt
if [ -d /opt/sovereign-os/scripts ]; then
  echo "sovereign-os scripts already present"
else
  echo "first-boot hook: sovereign-os scripts to be staged at install time"
fi
EOF
chmod +x "${out_dir}/config/hooks/normal/0010-sovereign-first-boot.hook.chroot"

# ---- Empty includes.chroot — populated by whitelabel render step ----
mkdir -p "${out_dir}/config/includes.chroot"

# ---- README at output root ----
cat > "${out_dir}/README.md" <<EOF
# live-build config for sovereign-os profile \`${profile_id}\`

Auto-generated by \`scripts/build/adapters/live-build-emit.sh\`.

Build:
    cd <this dir>
    sudo lb build

Config:
    - config/auto/config   — lb_config flags (Debian Trixie, ISO-hybrid)
    - config/package-lists/sovereign.list.chroot   — from profile.packages
    - config/includes.chroot/   — populated by whitelabel render
    - config/hooks/normal/      — first-boot hook

NOTE: This is the ALT-A substrate path (live-build). The PRIMARY path
per SDD-003 recommendation is mkosi-on-Debian-13. Adapter exists so
operator can swap at Stage Gate 2 without rewriting profile bodies.
EOF

log_info "live-build config emitted to ${out_dir}"
log_info "  files:"
find "${out_dir}" -maxdepth 4 -type f | while read -r f; do
  log_info "    ${f}"
done
