#!/usr/bin/env bash
# scripts/build/04-kernel-compile.sh — compile the kernel into .deb
# packages via bindeb-pkg (per E101).

__SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
# shellcheck source=./lib/common.sh
. "${__SCRIPT_DIR}/lib/common.sh"
# shellcheck source=./lib/observability.sh
. "${__SCRIPT_DIR}/lib/observability.sh"

STEP_ID="04-kernel-compile"

: "${SOVEREIGN_OS_PROFILE:=sain-01}"
load_profile "${SOVEREIGN_OS_PROFILE}"

: "${SOVEREIGN_OS_FORGE_DIR:=/mnt/kernel_forge}"
: "${SOVEREIGN_OS_KERNEL_SRC:=${SOVEREIGN_OS_FORGE_DIR}/linux-stable}"
: "${SOVEREIGN_OS_PARALLEL:=$(nproc)}"

# Q18-A: substrate-default profiles skip kernel-build steps 02-04.
kernel_source="$(profile_field kernel.source)"
if [ "${kernel_source}" = "substrate-default" ] || [ -z "${kernel_source}" ]; then
  log_info "skipping ${STEP_ID} (kernel.source=substrate-default — Debian archive supplies the .deb)"
  exit 0
fi

# Reproducibility (SDD-019): when SOURCE_DATE_EPOCH is set, propagate
# it to the kernel build. This drives KBUILD_BUILD_TIMESTAMP +
# embedded mtimes in the .deb. Folding it into inputs_hash ensures the
# state machine notices when the operator pins a new epoch.
if [ -n "${SOURCE_DATE_EPOCH:-}" ]; then
  export SOURCE_DATE_EPOCH
  KBUILD_BUILD_TIMESTAMP="$(date -u -d "@${SOURCE_DATE_EPOCH}" '+%a %b %d %H:%M:%S UTC %Y' 2>/dev/null || date -u '+%a %b %d %H:%M:%S UTC %Y')"
  export KBUILD_BUILD_TIMESTAMP
  inputs_hash="$(state_inputs_hash "${BASH_SOURCE[0]}" "${SOVEREIGN_OS_PROFILE_FILE}" "${SOVEREIGN_OS_STATE_DIR}/kernel.config" "epoch=${SOURCE_DATE_EPOCH}")"
else
  inputs_hash="$(state_inputs_hash "${BASH_SOURCE[0]}" "${SOVEREIGN_OS_PROFILE_FILE}" "${SOVEREIGN_OS_STATE_DIR}/kernel.config")"
fi

if ! state_step_should_run "${STEP_ID}" "${inputs_hash}"; then
  log_info "step ${STEP_ID} already completed with matching inputs — skipping"
  exit 0
fi

log_step_header "${STEP_ID}" "compile kernel (bindeb-pkg)"
state_step_start "${STEP_ID}" "${inputs_hash}"

require_dir "${SOVEREIGN_OS_KERNEL_SRC}"
require_command make
require_command dpkg-deb

# Source the env handoff from step 03
env_file="${SOVEREIGN_OS_STATE_DIR}/env-kernel-config.sh"
require_file "${env_file}"
# shellcheck disable=SC1090
. "${env_file}"

cd "${SOVEREIGN_OS_KERNEL_SRC}" || exit 1

# ---- packaging choice ----
packaging="$(profile_field kernel.packaging)"
case "${packaging}" in
  bindeb-pkg|substrate-default|"")
    log_info "packaging: bindeb-pkg (parallel=${SOVEREIGN_OS_PARALLEL})"
    ;;
  rpm)
    log_error "rpm packaging not yet implemented (target = Debian path)"
    state_step_fail "${STEP_ID}" "rpm-packaging-not-implemented"
    exit 1
    ;;
  nix)
    log_error "nix packaging not yet implemented"
    state_step_fail "${STEP_ID}" "nix-packaging-not-implemented"
    exit 1
    ;;
  *)
    log_error "unknown packaging: ${packaging}"
    state_step_fail "${STEP_ID}" "unknown-packaging"
    exit 1
    ;;
esac

# ---- compile ----
if [ -n "${SOURCE_DATE_EPOCH:-}" ]; then
  log_info "reproducibility: SOURCE_DATE_EPOCH=${SOURCE_DATE_EPOCH} → KBUILD_BUILD_TIMESTAMP=${KBUILD_BUILD_TIMESTAMP}"
fi

log_info "starting kernel compile (this can take 30+ minutes on SAIN-01-class hardware)"

if [ -n "${SOVEREIGN_OS_DRY_RUN:-}" ]; then
  log_warn "SOVEREIGN_OS_DRY_RUN set — skipping actual compile"
else
  # bindeb-pkg generates .deb packages in the parent directory of the
  # kernel source tree. KCFLAGS / KCPPFLAGS / KBUILD_BUILD_USER /
  # KBUILD_BUILD_HOST already exported from env handoff.
  time make -j"${SOVEREIGN_OS_PARALLEL}" bindeb-pkg 2>&1 | tee "${SOVEREIGN_OS_LOG_DIR}/kernel-compile-${SOVEREIGN_OS_BUILD_ID}.log"
fi

# ---- collect output ----
output_dir="${SOVEREIGN_OS_FORGE_DIR}/kernel-debs"
mkdir -p "${output_dir}"

if [ -z "${SOVEREIGN_OS_DRY_RUN:-}" ]; then
  parent="$(cd "${SOVEREIGN_OS_KERNEL_SRC}/.." && pwd)"
  shopt -s nullglob
  produced=("${parent}"/linux-*-*.deb)
  shopt -u nullglob
  if [ "${#produced[@]}" -eq 0 ]; then
    log_error "no .deb files produced; check ${SOVEREIGN_OS_LOG_DIR}/kernel-compile-*.log"
    state_step_fail "${STEP_ID}" "no-deb-output"
    exit 1
  fi
  mv "${produced[@]}" "${output_dir}/"
  log_info "kernel .debs produced (${#produced[@]} files):"
  for f in "${output_dir}"/*.deb; do
    log_info "  $(basename "$f")  ($(du -h "$f" | cut -f1))"
  done
fi

env_file_out="${SOVEREIGN_OS_STATE_DIR}/env-kernel-debs.sh"
cat > "${env_file_out}" <<EOF
# auto-generated by ${STEP_ID}
export SOVEREIGN_OS_KERNEL_DEBS_DIR="${output_dir}"
EOF
log_info "env handoff: ${env_file_out}"

emit_metric sovereign_os_build_step_kernel_compile_total 1 \
  "profile=\"${SOVEREIGN_OS_PROFILE}\",result=\"success\""
state_step_complete "${STEP_ID}"
log_info "step ${STEP_ID} complete"
