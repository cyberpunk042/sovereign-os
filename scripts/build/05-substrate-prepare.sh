#!/usr/bin/env bash
# scripts/build/05-substrate-prepare.sh — substrate-adapter prep.
#
# Reads the active profile + active substrate (per Q-001 Gate 2);
# emits substrate-native config (mkosi.conf / live-build config/ /
# treefile.yaml / configuration.nix). Working hypothesis substrate:
# mkosi on Debian 13 (per SDD-003 primary recommendation). Operator
# overrides via SOVEREIGN_OS_SUBSTRATE env var until Gate 2 locks.

__SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
# shellcheck source=./lib/common.sh
. "${__SCRIPT_DIR}/lib/common.sh"
# shellcheck source=./lib/observability.sh
. "${__SCRIPT_DIR}/lib/observability.sh"

STEP_ID="05-substrate-prepare"

: "${SOVEREIGN_OS_PROFILE:=sain-01}"
: "${SOVEREIGN_OS_SUBSTRATE:=mkosi}"
load_profile "${SOVEREIGN_OS_PROFILE}"

emit_substrate_metric() {
  emit_metric sovereign_os_build_step_substrate_total 1 \
    "substrate=\"${SOVEREIGN_OS_SUBSTRATE}\",profile=\"${SOVEREIGN_OS_PROFILE}\",result=\"$1\""
}

# The emitted config is a function of the ADAPTER CODE and the env knobs
# below, not just this step + the profile: a fixed emitter (or a changed
# snapshot pin / signing key / root-password posture) silently reused the
# stale build/<profile>/mkosi.conf because none of them were hashed —
# the 2026-06-12 Run-console build failed on main-only apt AFTER the
# repos fix landed, because step 05 skipped itself.
adapter_for_hash="${__SCRIPT_DIR}/adapters/${SOVEREIGN_OS_SUBSTRATE}-emit.sh"
inputs_hash="$(state_inputs_hash "${BASH_SOURCE[0]}" "${SOVEREIGN_OS_PROFILE_FILE}" \
  "${adapter_for_hash}" \
  "snapshot=${DEBIAN_SNAPSHOT:-}" \
  "epoch=${SOURCE_DATE_EPOCH:-}" \
  "sb_key=${SOVEREIGN_OS_PK_KEY:-${SOVEREIGN_OS_MOK_KEY:-}}" \
  "root_pw=$([ -n "${SOVEREIGN_OS_ROOT_PASSWORD:-}" ] && echo set || echo unset)" \
  "bake_dev=${SOVEREIGN_OS_BAKE_DEV_TOOLS:-}" \
  "bake_selfdef=${SOVEREIGN_OS_BAKE_SELFDEF:-}" \
  "node_major=${SOVEREIGN_OS_NODE_MAJOR:-22}")"

if ! state_step_should_run "${STEP_ID}" "${inputs_hash}"; then
  log_info "step ${STEP_ID} already completed with matching inputs — skipping"
  exit 0
fi

log_step_header "${STEP_ID}" "substrate-adapter prep (substrate=${SOVEREIGN_OS_SUBSTRATE})"
state_step_start "${STEP_ID}" "${inputs_hash}"

: "${SOVEREIGN_OS_BUILD_OUT:=${SOVEREIGN_OS_ROOT}/build/${SOVEREIGN_OS_PROFILE}}"
mkdir -p "${SOVEREIGN_OS_BUILD_OUT}"

case "${SOVEREIGN_OS_SUBSTRATE}" in
  mkosi)
    adapter="${__SCRIPT_DIR}/adapters/mkosi-emit.sh"
    ;;
  live-build)
    adapter="${__SCRIPT_DIR}/adapters/live-build-emit.sh"
    ;;
  rpm-ostree|nixos)
    log_error "substrate '${SOVEREIGN_OS_SUBSTRATE}' is an ALT path (B/C); adapter not implemented in foundation phase"
    emit_substrate_metric not-implemented
    state_step_fail "${STEP_ID}" "alt-substrate-not-implemented"
    exit 1
    ;;
  *)
    log_error "unknown substrate: ${SOVEREIGN_OS_SUBSTRATE} (valid: mkosi, live-build, rpm-ostree, nixos)"
    emit_substrate_metric unknown
    state_step_fail "${STEP_ID}" "unknown-substrate"
    exit 1
    ;;
esac

if [ ! -x "${adapter}" ]; then
  log_error "${SOVEREIGN_OS_SUBSTRATE} adapter missing or not executable: ${adapter}"
  emit_substrate_metric missing-adapter
  state_step_fail "${STEP_ID}" "missing-${SOVEREIGN_OS_SUBSTRATE}-adapter"
  exit 1
fi

if [ -n "${SOVEREIGN_OS_DRY_RUN:-}" ]; then
  log_info "DRY-RUN — would invoke ${adapter} ${SOVEREIGN_OS_PROFILE_FILE} ${SOVEREIGN_OS_BUILD_OUT}"
  emit_substrate_metric skip
  # Record 'dry-run', NOT 'completed' — completing here with the real
  # inputs_hash makes the next REAL run skip this step body entirely.
  state_step_dry_run "${STEP_ID}"
  exit 0
fi

if "${adapter}" "${SOVEREIGN_OS_PROFILE_FILE}" "${SOVEREIGN_OS_BUILD_OUT}"; then
  log_info "${SOVEREIGN_OS_SUBSTRATE} adapter emit succeeded → ${SOVEREIGN_OS_BUILD_OUT}"
  emit_substrate_metric success
else
  rc=$?
  log_error "${SOVEREIGN_OS_SUBSTRATE} adapter emit failed (rc=${rc})"
  emit_substrate_metric fail
  state_step_fail "${STEP_ID}" "adapter-emit-failed-${rc}"
  exit 1
fi

env_file="${SOVEREIGN_OS_STATE_DIR}/env-substrate.sh"
cat > "${env_file}" <<EOF
# auto-generated by ${STEP_ID}
export SOVEREIGN_OS_SUBSTRATE="${SOVEREIGN_OS_SUBSTRATE}"
export SOVEREIGN_OS_BUILD_OUT="${SOVEREIGN_OS_BUILD_OUT}"
EOF
log_info "env handoff: ${env_file}"

state_step_complete "${STEP_ID}"
log_info "step ${STEP_ID} complete"
